    <script type="text/javascript">
        $(function(){
            //bắt sự kiện click sign in
            $('button[type="button"]').click(function(){
                // alert(1);
                var username = $.trim($('#txtUsername').val());
                var password = $.trim($('#txtPass').val());
                $.ajax({
                    url : "?c=login&m=handle",
                    type :"POST",
                    data : {username :username, password :password},
                    beforeSend : function(){
                        $('button[type="button"]').text('Loading....');
                    },
                    success : function(respone){
                        $('button[type="button"]').text('Sign in');
                        respone = $.trim(respone);
                        if (respone==='Empty') {
                            alert('Vui lòng nhập thông tin');
                        }else if (respone === 'FAIL') {
                            alert('Username hoặc Password không đúng!');
                        }else if (respone === 'OK') {
                            window.location.href = "?c=dashboard";
                        }
                        //không làm gì nữa,dừng lại chương trình
                        // return false;
                    }
                });
            });
        })
